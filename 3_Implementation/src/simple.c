#include <math.h>
#include "../inc/simple.h"
#include "../unity/unity.h"

// addition function
int add_res(int a, int b)
{
    return (a + b);
}

// subtraction function
int sub_res(int a, int b)
{
    return (a - b);
}

// multiplication function
int mul_res(int a, int b)
{
    return (a * b);
}

// division function
float div_res(int a, int b)
{
    return (a / b);
}
